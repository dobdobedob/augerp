package kr.happyjob.study.accounting.model;

public class EmpDvModel {

	private int exp_no; 
	private String exp_date; 
	private String exp_sdate; 
	private String acc_nm; 
	private String dac_nm; 
	private int dept_cd; 
	private int exp_cost; 
	private String exp_stat;
	public int getExp_no() {
		return exp_no;
	}
	public void setExp_no(int exp_no) {
		this.exp_no = exp_no;
	}
	public String getExp_date() {
		return exp_date;
	}
	public void setExp_date(String exp_date) {
		this.exp_date = exp_date;
	}
	public String getExp_sdate() {
		return exp_sdate;
	}
	public void setExp_sdate(String exp_sdate) {
		this.exp_sdate = exp_sdate;
	}
	public String getAcc_nm() {
		return acc_nm;
	}
	public void setAcc_nm(String acc_nm) {
		this.acc_nm = acc_nm;
	}
	public String getDac_nm() {
		return dac_nm;
	}
	public void setDac_nm(String dac_nm) {
		this.dac_nm = dac_nm;
	}
	public int getDept_cd() {
		return dept_cd;
	}
	public void setDept_cd(int dept_cd) {
		this.dept_cd = dept_cd;
	}
	public int getExp_cost() {
		return exp_cost;
	}
	public void setExp_cost(int exp_cost) {
		this.exp_cost = exp_cost;
	}
	public String getExp_stat() {
		return exp_stat;
	}
	public void setExp_stat(String exp_stat) {
		this.exp_stat = exp_stat;
	} 
	
}
